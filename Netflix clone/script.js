let ans1 = document.getElementById("ans1");
let quesAns_box1 = document.getElementById("quesAns_box1")
let icon1 = document.getElementById("icon1")

quesAns_box1.addEventListener('click', drop1)
function drop1(){
    if(ans1.style.display === "block"){
        ans1.style.display = 'none'
        icon1.style.transform = null
    }
    else{
        ans1.style.display = "block"
        icon1.style.transform = 'rotate(45deg)'
    }
}

